export function createRepairFallback(files: SandpackFiles, issue: string) {
  const fixedFiles = {
    ...files,
    "/src/App.tsx": {
      ...(files["/src/App.tsx"] ?? DEFAULT_FILES["/src/App.tsx"]),
      active: true,
      code: `${files["/src/App.tsx"]?.code ?? DEFAULT_FILES["/src/App.tsx"].code}

// Fix note: ${issue.replace(/\*\//g, "")}
`,
    },
  };

  return {
    files: fixedFiles,
    fixSummary: "本地修复兜底已记录问题说明；配置可用模型密钥后会由 Alex 智能体重写相关代码。",
    agentEvents: [
      event("System", "Issue Report", "收到错误报告", issue, "done"),
      event("Alex", "Software Engineer", "应用兜底修复", "已保留当前可运行文件并追加修复说明。", "done"),
    ],
    usedFallback: true,
  };
}

export function createAgentEvents(mode: BuilderMode, prompt: string, fallback = false): AgentEvent[] {
  const actors =
    mode === "engineer"
      ? [["Alex", "Software Engineer", "实现原型"]]
      : [
          ["Mike", "Team Leader", "拆解任务"],
          ["Emma", "Product Manager", "梳理 PRD"],
          ["Bob", "System Architect", "设计结构"],
          ["Alex", "Software Engineer", "生成代码"],
          ["Sarah", "SEO Specialist", "优化文案"],
        ];

  return actors.map(([agent, role, title], index) =>
    event(
      agent as AgentEvent["agent"],
      role,
      title,
      index === 0
        ? `围绕“${prompt.slice(0, 80)}”启动构建${fallback ? "，当前使用本地兜底。" : "。"}`
        : "已完成该阶段并交付给下一位智能体。",
      "done",
    ),
  );
}

function event(agent: AgentEvent["agent"], role: string, title: string, detail: string, status: AgentEvent["status"]) {
  return {
    id: createId("event"),
    agent,
    role,
    title,
    detail,
    status,
    createdAt: nowIso(),
  };
}

function titleFromPrompt(prompt: string, variant: number) {
  const cleaned = prompt
    .replace(/[^\p{L}\p{N}\s-]/gu, "")
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 6)
    .join(" ");
  return cleaned ? `${cleaned}${variant ? " Lab" : ""}` : "AI Generated App";
}

function modeLabel(mode: BuilderMode) {
  if (mode === "engineer") return "Engineer Mode";
  if (mode === "race") return "Race Mode";
  return "Team Mode";
}

function isResumePrompt(prompt: string) {
  return /简历|履历|求职|候选人|resume|cv|评分|评估|建议/i.test(prompt);
}

function stringLiteral(value: string) {
  return JSON.stringify(value);
}
