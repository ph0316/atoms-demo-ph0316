const keywordGroups = [
  { label: "岗位匹配", keywords: ["react", "typescript", "前端", "ai", "简历", "评估", "产品", "项目"] },
  { label: "能力证据", keywords: ["主导", "负责", "落地", "优化", "增长", "指标", "测试", "上线"] },
  { label: "表达清晰度", keywords: ["目标", "亮点", "项目", "结果", "数据", "协作", "架构", "用户"] }
];

function clamp(value: number) {
  return Math.max(0, Math.min(100, value));
}

function scoreGroup(text: string, keywords: string[], base: number) {
  const lower = text.toLowerCase();
  const hits = keywords.filter((keyword) => lower.includes(keyword.toLowerCase())).length;
  const density = Math.min(text.length / 900, 1) * 18;
  return clamp(Math.round(base + hits * 8 + density));
}

function analyze(text: string) {
  const content = text.trim();
  const lengthBoost = clamp(Math.round(content.length / 18));
  const scores = keywordGroups.map((group, index) => ({
    label: group.label,
    score: scoreGroup(content, group.keywords, 48 + index * 3)
  }));
  const total = clamp(Math.round(scores.reduce((sum, item) => sum + item.score, 0) / scores.length + lengthBoost * 0.16));
  const suggestions = [
    scores[0].score < 78 ? "补充目标岗位关键词，让经历和岗位要求形成直接对应。" : "岗位关键词覆盖较好，可以继续突出业务场景。",
    scores[1].score < 78 ? "每段经历增加可量化结果，例如转化率、效率、成本或稳定性指标。" : "能力证据比较充分，建议保留最有代表性的 2-3 个成果。",
    scores[2].score < 78 ? "把经历改写成“动作 + 方法 + 结果”的结构，减少泛泛描述。" : "表达结构清楚，可以把摘要区再压缩成更强的开场。"
  ];

  return { total, scores, suggestions, ready: content.length > 40 };
}

