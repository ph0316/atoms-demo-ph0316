export function createFallbackGeneration(prompt: string, mode: BuilderMode, variant = 0): GenerateResponse {
  const projectName = titleFromPrompt(prompt, variant);
  const accent = variant === 1 ? "#0f8f72" : "#334bfa";
  const warm = variant === 1 ? "#fff7e2" : "#eef3ff";
  const resumePrompt = isResumePrompt(prompt);
  const files = resumePrompt
    ? createResumeEvaluatorFiles(projectName, prompt, mode, accent, warm)
    : createGenericFiles(projectName, prompt, mode, accent, warm);
  const summary = resumePrompt
    ? "真实模型暂不可用或返回不可解析内容，已用本地兜底生成一个可运行的 AI 简历评估工具。"
    : "真实模型暂不可用或返回不可解析内容，已用本地规则生成一个可运行 React 原型，便于完整演示工作流。";

  const base: GenerateResponse = {
    projectName,
    mode,
    model: "local-fallback",
    usedFallback: true,
    agentEvents: createAgentEvents(mode, prompt, true),
    files,
    summary,
    checks: ["生成了必要 Sandpack 文件", "限制依赖为白名单", "可继续通过修复接口迭代"],
  };

  if (mode === "race") {
    const other = createFallbackGeneration(prompt, "team", 1);
    const labels = resumePrompt
      ? ["方案 A：HR 评分工作台", "方案 B：候选人建议面板"]
      : ["方案 A：高转化首页", "方案 B：运营看板风格"];

    return {
      ...base,
      candidates: [
        {
          id: createId("candidate"),
          label: labels[0],
          model: "local-fallback-a",
          summary: base.summary,
          files: base.files,
          checks: base.checks,
        },
        {
          id: createId("candidate"),
          label: labels[1],
          model: "local-fallback-b",
          summary: other.summary,
          files: other.files,
          checks: other.checks,
        },
      ],
    };
  }

  return base;
}

